func update(s string) string {
	result := ""
	for i := 0; i < len(s); i++ {
		if s[i] >= 'A' && s[i] < 'A'+26 {
			result += string(s[i]-'A'+'a')
		} else if s[i] >= 'a' && s[i] < 'a'+26 {
			result += string(s[i])
		}
	}
	return result
}


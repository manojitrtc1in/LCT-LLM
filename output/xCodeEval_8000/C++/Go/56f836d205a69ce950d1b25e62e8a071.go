func isLucky(n int) bool {
	if n == 2 {
		return true
	}
	if n < 3 {
		return false
	}
	if n%4 != 1 {
		return false
	}
	for i := 2; i*i <= n; i++ {
		if n%i == 0 {
			return false
		}
	}
	return true
}

const MAXN = 300*1000*1000 + 5

var precalc map[int]int

func get(border int) int {
	solved := 0
	result := 0
	it := precalc[border]
	if it != 0 {
		solved = it
		result = precalc[solved]
	}
	for i := solved + 1; i <= border; i++ {
		if isLucky(i) {
			result++
		}
	}
	return result
}


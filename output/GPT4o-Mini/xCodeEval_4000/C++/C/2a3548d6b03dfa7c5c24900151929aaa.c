#define MAX_STR_LEN 100
#define MAX_MAP_SIZE 200

typedef struct {
    int first;
    int second;
} pii;

pii ma[MAX_MAP_SIZE];
char keys[MAX_MAP_SIZE][MAX_STR_LEN];
int ma_size = 0;

void add_to_map(const char* key, int first, int second) {
    strcpy(keys[ma_size], key);
    ma[ma_size].first = first;
    ma[ma_size].second = second;
    ma_size++;
}

int find_in_map(const char* key) {
    for (int i = 0; i < ma_size; i++) {
        if (strcmp(keys[i], key) == 0) {
            return i;
        }
    }
    return -1;
}


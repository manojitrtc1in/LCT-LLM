    char s[100];
    int a[4] = {0, 0, 0, 0};

    while (scanf("%s", s) != EOF) {
        int index = find_in_map(s);
        if (index != -1) {
            a[ma[index].first] += ma[index].second;
        }
    }

    int m = a[1];
    if (a[2] > m) m = a[2];
    if (a[3] > m) m = a[3];

    if (a[1] == m) {
        printf("1\n");
    } else if (a[2] == m) {
        printf("2\n");
    } else {
        printf("3\n");
    }

    return 0;

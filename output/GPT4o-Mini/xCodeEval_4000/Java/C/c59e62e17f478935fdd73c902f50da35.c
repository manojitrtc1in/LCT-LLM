int step = 1000000;

int perfect(int i) {
    int f1 = -1, f2 = -1;

    while (i > 0) {
        int lastDig = i % 10;

        if (f1 == -1)
            f1 = lastDig;
        if (f2 == -1 && lastDig != f1)
            f2 = lastDig;

        if (f1 != -1 && f2 != -1 && (lastDig != f1 && lastDig != f2))
            return 0;

        i /= 10;
    }

    return 1;
}

int main() {
    int n;
    scanf("%d", &n);

    int b = n / step;

    int count = values[b];
    for (int i = b * step + 1; i <= n; i++) {
        if (perfect(i)) {
            count++;
        }
    }

    printf("%d\n", count - 1);
    return 0;
}

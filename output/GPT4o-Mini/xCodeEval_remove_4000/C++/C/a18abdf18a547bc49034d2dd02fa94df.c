typedef struct {
    int first;
    int second;
} pii;

typedef struct {
    char key[50];
    pii value;
} map_entry;

map_entry ma[200];
int ma_size = 0;

void insert(const char* key, int first, int second) {
    strcpy(ma[ma_size].key, key);
    ma[ma_size].value.first = first;
    ma[ma_size].value.second = second;
    ma_size++;
}

int find(const char* key) {
    for (int i = 0; i < ma_size; i++) {
        if (strcmp(ma[i].key, key) == 0) {
            return i;
        }
    }
    return -1;
}


func isLucky(n int) bool {
	if n == 2 {
		return true
	}
	if n < 3 {
		return false
	}
	if n%4 != 1 {
		return false
	}
	for i := 2; i*i <= n; i++ {
		if n%i == 0 {
			return false
		}
	}
	return true
}


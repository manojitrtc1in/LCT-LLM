class BIT:
    def __init__(self, len):
        self.dt = [0] * len

    def add(self, a, x):
        while a < len:
            self.dt[a] += x
            a += a & -a

    def add(self, l, r, x):
        self.add(l, x)
        self.add(r + 1, -x)

    def get(self, a):
        ans = 0
        while a:
            ans += self.dt[a]
            a -= a & -a
        return ans

    def get(self, l, r):
        return self.get(r) - self.get(l - 1)


class point:
    def __init__(self, x, y):
        self.x = x
        self.y = y
        self.ang = math.atan2(y, x)

    def __add__(self, pt):
        return point(self.x + pt.x, self.y + pt.y)

    def __sub__(self, pt):
        return point(self.x - pt.x, self.y - pt.y)

    def __mul__(self, a):
        return point(self.x * a, self.y * a)

    def __truediv__(self, a):
        return point(self.x / a, self.y / a)

    def __mul__(self, pt):
        return self.x * pt.x + self.y * pt.y

    def __mod__(self, pt):
        return self.x * pt.y - self.y * pt.x

    def length(self):
        return self.x * self.x + self.y * self.y

    def __xor__(self, pt):
        return math.acos(self * pt) / math.sqrt(self.length() * pt.length())

    def getangle(self):
        self.ang = math.atan2(self.y, self.x)
        return self.ang

    def __lt__(self, p):
        return self.ang < p.ang


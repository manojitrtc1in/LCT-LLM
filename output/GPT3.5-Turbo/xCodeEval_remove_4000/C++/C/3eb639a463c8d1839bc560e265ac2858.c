typedef struct {
    int x;
    int y;
} pt;

typedef struct {
    int s;
    li sum;
    li minsum;
} node;

int n;
pt a[MAX_N];
node t[4 * MAX_N];
int vals[MAX_N];

int cmpfunc(const void* a, const void* b) {
    return (*(int*)a - *(int*)b);
}

node merge(node a, node b) {
    node c;
    c.s = 0;
    c.sum = a.sum + b.sum;
    c.minsum = (a.minsum < a.sum + b.minsum) ? a.minsum : a.sum + b.minsum;
    return c;
}

void build(int v, int l, int r) {
    if (l == r - 1) {
        if (l == 0) {
            t[v].s = 0;
            t[v].sum = INF + 1;
        } else {
            t[v].s = 0;
            t[v].sum = 0;
        }
        return;
    }
    int m = (l + r) / 2;
    build(v * 2, l, m);
    build(v * 2 + 1, m, r);
    t[v] = merge(t[v * 2], t[v * 2 + 1]);
}

void upd(int v, int l, int r, int pos, pt val) {
    if (l == r - 1) {
        t[v].s = val.x;
        t[v].sum = val.y;
        return;
    }
    int m = (l + r) / 2;
    if (pos < m)
        upd(v * 2, l, m, pos, val);
    else
        upd(v * 2 + 1, m, r, pos, val);
    t[v] = merge(t[v * 2], t[v * 2 + 1]);
}

ld ask(int v, int l, int r, int L, int R, li* x) {
    if (L >= R)
        return 2 * INF;
    if (l == L && r == R) {
        if (t[v].minsum + *x > 0) {
            *x += t[v].sum;
            return 2 * INF;
        }
        while (l < r - 1) {
            int m = (l + r) / 2;
            if (t[v * 2].minsum + *x <= 0) {
                r = m;
                v = v * 2;
            } else {
                *x += t[v * 2].sum;
                l = m;
                v = v * 2 + 1;
            }
        }
        if (*x == 0)
            return vals[l];
        assert(t[v].minsum + *x <= 0);
        assert(t[v].s < 0);
        ld res = vals[l] + (*x / (ld)(-t[v].s));
        *x = 0;
        return res;
    }
    int m = (l + r) / 2;
    ld tmp = ask(v * 2, l, m, L, (m < R) ? m : R, x);
    if (*x == 0)
        return tmp;
    assert(*x > 0);
    return ask(v * 2 + 1, m, r, (m > L) ? m : L, R, x);
}


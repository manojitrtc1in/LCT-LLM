#define ll long long

typedef struct {
    ll* data;
    int size;
} Array;

typedef struct {
    Array* fact;
    Array* fact_inv;
    ll mod;
} Combination;

Array* createArray(int size) {
    Array* array = (Array*)malloc(sizeof(Array));
    array->data = (ll*)malloc(size * sizeof(ll));
    array->size = size;
    return array;
}

void destroyArray(Array* array) {
    free(array->data);
    free(array);
}

Combination* createCombination(ll n, ll mod) {
    Combination* combination = (Combination*)malloc(sizeof(Combination));
    combination->fact = createArray(n);
    combination->fact_inv = createArray(n);
    combination->mod = mod;
    return combination;
}

void destroyCombination(Combination* combination) {
    destroyArray(combination->fact);
    destroyArray(combination->fact_inv);
    free(combination);
}

ll mod_pow(ll x, ll n, ll mod) {
    ll res = 1;
    while (n > 0) {
        if (n & 1) res = res * x % mod;
        x = x * x % mod;
        n >>= 1;
    }
    return res;
}

ll mod_inv(ll x, ll mod) {
    return mod_pow(x, mod - 2, mod);
}

void initCombination(Combination* combination, ll n) {
    int i;
    combination->fact->data[0] = 1;
    for (i = 1; i < n; i++) {
        combination->fact->data[i] = (combination->fact->data[i - 1] * i) % combination->mod;
    }
    combination->fact_inv->data[n - 1] = mod_inv(combination->fact->data[n - 1], combination->mod);
    for (i = n - 2; i >= 0; i--) {
        combination->fact_inv->data[i] = (combination->fact_inv->data[i + 1] * (i + 1)) % combination->mod;
    }
}

ll nCr(Combination* combination, ll n, ll r) {
    if (n < r) return 0;
    if (n < combination->mod) return ((combination->fact->data[n] * combination->fact_inv->data[r] % combination->mod) * combination->fact_inv->data[n - r]) % combination->mod;

    ll ret = 1;
    while (n || r) {
        ll _n = n % combination->mod, _r = r % combination->mod;
        n /= combination->mod;
        r /= combination->mod;
        (ret *= nCr(combination, _n, _r)) %= combination->mod;
    }
    return ret;
}


typedef struct {
    unsigned int _v;
} mint;

mint raw(int v) {
    mint x;
    x._v = v;
    return x;
}

mint add(mint a, mint b, int m) {
    mint c;
    c._v = a._v + b._v;
    if (c._v >= m) c._v -= m;
    return c;
}

mint sub(mint a, mint b, int m) {
    mint c;
    c._v = a._v + m - b._v;
    if (c._v >= m) c._v -= m;
    return c;
}

mint mul(mint a, mint b, int m) {
    mint c;
    c._v = (unsigned long long)(a._v) * b._v % m;
    return c;
}


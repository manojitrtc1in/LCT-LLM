typedef struct {
    unsigned int _v;
} mint;

mint raw(int v) {
    mint x;
    x._v = v;
    return x;
}

mint add(mint a, mint b, int m) {
    mint c;
    c._v = a._v + b._v;
    if (c._v >= m) c._v -= m;
    return c;
}

mint sub(mint a, mint b, int m) {
    mint c;
    c._v = a._v + m - b._v;
    if (c._v >= m) c._v -= m;
    return c;
}

mint mul(mint a, mint b, int m) {
    mint c;
    c._v = (unsigned long long)(a._v) * b._v % m;
    return c;
}

mint pow_mod(mint x, long long n, int m) {
    if (m == 1) return raw(0);
    unsigned int _m = (unsigned int)(m);
    unsigned long long r = 1;
    unsigned long long y = x._v;
    while (n) {
        if (n & 1) r = r * y % _m;
        y = y * y % _m;
        n >>= 1;
    }
    return raw(r);
}

mint inv(mint x, int m) {
    unsigned int a = x._v, b = m, u = 1, v = 0;
    while (b) {
        unsigned int t = a / b;
        a -= t * b;
        u -= t * v;
        unsigned int tmp = a;
        a = b;
        b = tmp;
        tmp = u;
        u = v;
        v = tmp;
    }
    return raw(u);
}


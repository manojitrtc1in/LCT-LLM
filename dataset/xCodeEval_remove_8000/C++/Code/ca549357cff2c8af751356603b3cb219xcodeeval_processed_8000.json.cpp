unsigned int id0(unsigned int a, unsigned int b) {
	if (a % b == 0)
		return b;
	if (b % a == 0)
		return a;

	if (a > b)
		return id0(a % b, b);
	return id0(a, b % a);
}

int NOD(int a, int b) {
	if (a < b) {
		swap(a, b);
	}
	while (a % b != 0) {
		a = a % b;
		swap(a, b);
	}
	return b;
}


int father[MAXN], ans[MAXN];

int findFather(int n)
{
    if (father[n] == n) return n;
    return father[n] = findFather(father[n]);
}

void merge(int a, int b)
{
    int fa = findFather(a), fb = findFather(b);
    if (fa == fb) return;
    father[fa] = fb;
}

void solve(int x, int y, int v)
{
    if (x > y) return;
    int t = findFather(x);
    while (t <= y)
    {
        if (t == findFather(t))
        {
            ans[t] = v;
            merge(t, t + 1);
            t++;
        }
        else
        {
            t = findFather(t);
        }
    }
}


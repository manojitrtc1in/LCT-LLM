int main()
{
	int t = 0;
	cin >> t;
	using bid = pair<int, int>;
	vector<bid> v(200010);
	for (int t0 = 0; t0 < t; ++t0)
	{
		int n = 0, ai = 0;
		cin >> n;
		
		for (int i = 1; i <= n; ++i)
		{
			cin >> ai;
			

			++(v[ai].first);
			v[ai].second = i;
		}
		bool f = false;
		for (int i = 1; i <= n; ++i)
		{
			if (v[i].first == 1)
			{
				cout << v[i].second;
				f = true;
				break;
			}
		}
		if (!f)
			cout << -1;
		cout << endl;
		for (int i = 1; i <= n; ++i)
		{
			v[i].first = 0;
			v[i].second = 0;
		}
	}
	return 0;
}





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































	

	

	

	

	

	

	

	

	

 

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	

	






















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































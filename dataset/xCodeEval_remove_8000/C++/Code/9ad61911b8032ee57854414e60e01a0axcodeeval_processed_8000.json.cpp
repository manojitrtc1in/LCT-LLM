void id0(vector<int>arr, int n, vector<int>prefixSum)
{
    prefixSum[0] = arr[0];
    for (int i = 0; i < n; i++)
        prefixSum[i+1] = prefixSum[i] + arr[i];
}

bool id1(int N)
{
    int digit = N % 10;
    while (N != 0)
    {
        int current_digit = N % 10;
        N = N / 10;
        if (current_digit != digit)
        {
            return 0;
        }
    }
    return 1;
}

int isPrime(long long number) {

	if (number == 0 || number == 1 || (number % 2 == 0 && number > 2)) {
		return 0;
	}

	else {
		for (long long i=3; i <= (long long)sqrt(number)+1; i++) {
			if (number % i == 0) {
				return 0;
			}
		}
		return 1;
	}
}

bool isPalindrome(string S)
{
    string P = S;
    reverse(P.begin(), P.end());
    if (S == P) {
        return 1;
    }
    else {
        return 0;
    }
}


int lcm(int a, int b)
{
    return (a / gcd(a, b)) * b;
}




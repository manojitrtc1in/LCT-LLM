typedef long long LL;
using namespace std;

int t, n, m;
LL sum[100005];

struct node
{
	int x, y;
	bool operator <(const node &rhs)const{
		return x > rhs.x;
	}
}a[100005];

int main()
{
	

	


	scanf("%d", &t);
	while(t--){
		scanf("%d %d", &n, &m);
		for (int i = 1; i <= m; i++) scanf("%d %d", &a[i].x, &a[i].y);
		sort(a + 1, a + 1 + m);
		for (int i = 1; i <= m; i++) sum[i] = sum[i - 1] + a[i].x;
		LL ans = 0;
		for (int i = 1; i <= m; i++){
			int l = 1, r = m, pos = 0;
			

			

			

			

			while(l <= r){
				int mid = (l + r) >> 1;
				if(a[mid].x >= a[i].y) pos = mid, l = mid + 1;
				else r = mid - 1;
			}
			

			if(pos > n) continue;
			if(pos >= i){
				int num = n - pos;
				ans = max(ans, sum[pos] + 1LL * num * a[i].y);
			}else{
				int num = n - pos - 1;
				ans = max(ans, sum[pos] + 1LL * num * a[i].y + a[i].x);
			}
		}
		ans = max(ans, sum[min(n, m)]);
		

		printf("%lld\n", ans);
	}

	return 0;
}

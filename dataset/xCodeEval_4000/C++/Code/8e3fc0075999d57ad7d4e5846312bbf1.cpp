void printDivisors(int n , unordered_set <int> &ms)
{
    

    for (int i=1; i<=sqrt(n); i++)
    {
        if (n%i == 0)
        {
            

            if (n/i == i)
                ms.insert(i);
 
            else 

                ms.insert(i);
                ms.insert(n/i);

        }
    }
}
































































































































































































































 


















            



















































































        










            




































































































































































































































































































































































































































































  
  


















































































int main(){
  int t;
  cin>>t;
  while(t--){
    int n,m;
    cin>>n>>m;
    int arr[n][m];
    for (int i=0;i<n;i++){
      string s;
      cin>>s;
      for (int j=0;j<s.size();j++){
        arr[i][j] = s[j]-'0';
      }
    }
    if (n==1 || m==1){
      cout<<"YES"<<endl;
      continue;
    }
    bool f = false;
    

    for (int i=0;i<n-1;i++){
      for (int j=0;j<m-1;j++){
        if (arr[i][j] + arr[i][j+1] + arr[i+1][j] + arr[i+1][j+1] ==3){
          f = true;
          break;
        } 
      }
      if (f){
        break;
      }
    }
    if (f){
      cout<<"NO"<<endl;
    }
    else{
      cout<<"YES"<<endl;
    }


  }
  return 0;
}
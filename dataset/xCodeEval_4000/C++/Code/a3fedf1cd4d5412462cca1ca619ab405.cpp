const int N = 1e5 + 10;
long long a[N];
int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0);
	memset(a, 0, sizeof(a));
		int n, m;
		cin >> n >> m;
		for (int i = 1; i <= n; i++)  cin >> a[i];

		long long ans = 0;

		for (ll i = 1; i <= n; i++)
		{
			ans += i * (n - i + 1);
			if (a[i] == a[i - 1])  ans -= (i - 1) * (n - i + 1);
		}

		while (m--)
		{
			long long i, x;
			cin >> i >> x;

		
			if (a[i] == x)
			{
				cout << ans << endl;
		   }
			else
			{
				if (x == a[i - 1])
				{
					ans -= (i - 1) * (n - i + 1);
				}
				if (x == a[i + 1])
				{
					ans -= i * (n - i);
				}
				if (a[i] == a[i - 1])
				{
					ans += (i - 1) * (n - i + 1);
				}
				if (a[i] == a[i + 1])
				{
					ans += i * (n - i);
				}
				cout << ans << endl;
				a[i] = x;
			}
		}


	return 0;
}
































































































































struct UnionFind {
    vector<ll> r;
    UnionFind(ll N) {
        r = vector<ll>(N, -1);
    }
    ll root(ll x) {
        if (r[x] < 0) return x;
        return r[x] = root(r[x]);
    }
    bool unite(ll x,ll y) {
        x = root(x);
        y = root(y);
        if (x == y) return false;
        if (r[x] > r[y]) swap(x, y);
        r[x] += r[y];
        r[y] = x;
        return true;
    }
    ll size(int x) {
        return -r[root(x)];
    }
};




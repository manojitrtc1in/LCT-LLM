using namespace std;
int main(int argc, char const *argv[])
{
    int n, d;
    cin >> n >> d;
    vector<long long> v;
    for (int i = 0; i < n; i++)
    {
        int y;
        cin >> y;
        v.push_back(y);
    }
    for (int i = 0; i < n; i++)
    {
        v[i] = (d / v[i]) + 1;
    }
    sort(v.begin(), v.end());
    for (int i = 1; i < n; i++)
    {
        v[i] = v[i] + v[i - 1];
    }
    int ans = upper_bound(v.begin(), v.end(), n)-v.begin();
    cout<<ans;
    return 0;
}

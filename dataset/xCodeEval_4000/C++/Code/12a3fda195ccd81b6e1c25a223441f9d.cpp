void solve() {
	int n;
	cin >> n;
	string s[n];
	map<string, bool> m;
	for (int i = 0; i < n; i++) {
		cin >> s[i];
		m[s[i]] = true;
	}
	for (int i = 0; i < n; i++) {
		bool ok = false;
		for (int j = 1; j < s[i].length(); j++) {
			string pref = s[i].substr(0, j), suff = s[i].substr(j, s[i].length() - j);
			if (m[pref] && m[suff]) {
				ok = true;
			}
		}
		cout << ok;
	}
	cout << endl;
}

int main() {
	int t;
	cin >> t;
	while (t--) {
		solve();
	}
	

}
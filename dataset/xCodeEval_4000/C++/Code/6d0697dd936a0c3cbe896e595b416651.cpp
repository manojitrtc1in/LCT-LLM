#include <bits/stdc++.h>
using namespace std;


























































































































ll n,k;
ll a[300010];
map<ll,ll>mp;
int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        int flag=0;
        mp.clear();
       scanf("%lld%lld",&n,&k);
       for(int i=1;i<=n;i++)
       {
           scanf("%lld",&a[i]);
           mp[a[i]]=1;
       }
       for(int i=1;i<=n;i++)
       {
           if(mp.find(a[i]+k)!=mp.end()&&mp[a[i]+k])
           {flag=1;break;}
       }
       if(flag) printf("YES\n");
       else printf("NO\n");
    }
}

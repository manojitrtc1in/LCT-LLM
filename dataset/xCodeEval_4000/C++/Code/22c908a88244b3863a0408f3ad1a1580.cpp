using namespace std;
int main(int argc, char const *argv[])
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        string str;
        cin >> str;
        vector<long long > v(n, 0);
        long long  sum = 0;
        for (int i = 0; i < str.size(); i++)
        {
            if (str[i] == 'L')
            {
                sum = sum + i;
            }
            else
            {
                sum = sum + n - i - 1;
            }
        }
        

        for (int i = 0; i < n; i++)
        {
            if (i < n / 2 && str[i] == 'L')
            {
                v[i] = (n - 2 * i - 1);
            }
            else if (i >= n / 2 && str[i] == 'R')
            {
                v[i] = 2 * i - n + 1;
            }
        }
        sort(v.begin(), v.end());
        

        

        

        

        

         vector<long long>p(n,0);
         p[0]=v[v.size()-1]+sum;
        for (int i = 1; i < v.size(); i++)
        {
            p[i] = v[v.size() - 1 - i] + p[i-1];
        }
        for (int i = 0; i < v.size(); i++)
        {
            cout << p[i] << " ";
        }
        cout << endl;
    }

    return 0;
}

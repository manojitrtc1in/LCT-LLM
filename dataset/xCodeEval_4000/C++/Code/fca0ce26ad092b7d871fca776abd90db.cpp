int main()
{
	int n,k,tmp;
	cin>>n>>k;
	vector<int> cap(100010,0);
	vector<long long> cost(100010,0);
	long long sum=0,sum_cc=0,sum_ac=0,sum_aa=0,sum_c=0,sum_a=0;
	for (int i=0;i<n;++i)
	{
		cin>>cost[i];
		sum +=cost[i];
	}
	cost[n]=cost[0];
	for (int i=0;i<k;++i)
	{
		cin>>tmp;
		cap[tmp-1]=1;
		sum_c +=cost[tmp-1];
	}
	cap[n]=cap[0];
	sum_a=sum-sum_c;
	sum=0;
	for (int i=0;i<n;++i)
	{
		if (cap[i]==1)
		{
			sum_c -=cost[i];
			sum_cc +=sum_c*cost[i];
			sum_ac +=sum_a*cost[i];
		}
		else if(cap[i+1]==0)
				sum_aa +=cost[i]*cost[i+1];
	}
	sum=sum_aa+sum_ac+sum_cc;
	cout<<sum<<endl;
	return 0;
}

	int n, i, x, y;
	long long ans = 0LL;

	cin >> n >> k;
	for (i = 1; i < n; ++i) {
		cin >> x >> y;
		edge[x].push_back(y);
		edge[y].push_back(x);
	}

	search(1, 0);

	for (i = 0; i <= k; ++i) (ans += f[1][i]) %= mod;

	cout << ans << endl;


using namespace std;
int main(int argc, char const *argv[])
{
    int t, d;
    cin >> t >> d;
    vector<int> v;
    for (int i = 0; i < t; i++)
    {
        int h;
        cin >> h;
        v.push_back(h);
    }
    sort(v.begin(),v.end());
    int ans=0;
    int lo=-1,hi=t-1;
    int sum=v[hi];
    while (hi>lo)
    {
        if (sum>d)
        {
            ans++;
            hi--;
            sum=0;
        }
        else{
            if (sum==0)
            {
                sum=sum+v[hi];
            }
            else
            {
                 sum=sum+v[hi];
                 lo++;
            }
        }
        
    }
    
     cout<<ans;
    return 0;
}

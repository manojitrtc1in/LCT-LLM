#include <map>
#include <iomanip>
#include <sstream>
using namespace std;
const int MAXN = 1e3;
vector<int> v[ MAXN ];
int ar [ MAXN ];
int ans [ MAXN ];
int main() {
	ios::sync_with_stdio(false) , cin.tie(0) , cout.tie(0);
		int n;
		cin >> n;
		for (int i = 0; i < 2 * n; i++) {
			int a;
			cin >> a;
			v [ a ].push_back(i);
		}
		bool flag = false;
		for (int i = 10 ; i < 100 ; i++) 
		{
			if (v [ i ].size() == 1) 
			{
				ans[ v [ i ] [ 0 ] ] = flag + 1;
				flag = !flag;
				ar [ !flag ]++;
				
			}
		}
		for (int i = 10 ; i < 100 ; i++) {
			if (v[ i ].size() >= 2) 
			{
				for (int j = 0 ; j < v [ i ].size() ; j++)
				{
					ans [ v [ i ] [ j ] ] = flag + 1;
					flag = !flag;
				}
				ar[ 0 ]++;
				ar[ 1 ]++;
			}
		}
		cout << ar [ 0 ] * ar [ 1 ] << endl;
		for (int i = 0; i < n * 2; i++) 
		{
			cout << ans [ i ] << ' ';
		}
	
}
const int inf = 1e9;
void solve() {
    int n;
    std::cin >> n;

    int min = inf, max = -inf;
    std::vector<int> x(n), t(n);
    for (int i = 0; i < n; i++) {
        std::cin >> x[i];
    }
    for (int i = 0; i < n; i++) {
        std::cin >> t[i];
        min = std::min(min, x[i] - t[i]);
        max = std::max(max, x[i] + t[i]);
    }
    std::cout << 0.5 * (min + max) << "\n";
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    std::cout << std::fixed;

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}















































































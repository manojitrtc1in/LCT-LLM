int n;


struct cmp{
	bool operator ()(const ll &a,const ll &b){
		return a>b;
	}
};

int main()
{
	int n;
	scanf("%d",&n);
	priority_queue<ll,vector<ll>,cmp> que;
	while(que.size())
		que.pop();
	ll ans=0;
	for(int i=1;i<=n;++i)
	{
		ll x;
		scanf("%lld",&x);
		que.push(x);

		if(que.top()<x)
		{
			ans=ans+x-que.top();
			que.pop();
			que.push(x);
		}
	}
	printf("%lld\n",ans);
}














































































































































































































































































































 


 





























































































































































 



 

 











 




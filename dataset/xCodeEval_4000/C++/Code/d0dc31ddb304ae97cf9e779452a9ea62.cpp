int add(int a, int b){
	a += b;
	if (a >= MOD)
		a -= MOD;
	if (a < 0)
		a += MOD;
	return a;
}

int mul(int a, int b){
	return a * li(b) % MOD;
}

int inv2 = (MOD + 1) / 2;

int dp[N];
int ans;

void dfs(int v, int p = -1){
	dp[v] = 0;
	int cur = 1;
	for (int u : g[v]) if (u != p){
		dfs(u, v);
		dp[v] = add(dp[v], mul(add(1, -dp[u]), mul(cur, inv2)));
		cur = mul(cur, add(mul(dp[u], inv2), inv2));
	}
	ans = add(ans, dp[v]);
	

}


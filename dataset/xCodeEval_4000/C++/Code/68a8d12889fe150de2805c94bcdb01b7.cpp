void solve() noexcept {
  int n, m;
  cin >> n >> m;
  vector<int> an(n);
  for (int i = 0; i < n; ++i) { cin >> an[i]; }
  sort(an.begin(), an.end());
  an.erase(unique(an.begin(), an.end()), an.end());
  vector<int> inv(m + 1, -1);
  int k = ssize(an);
  for (int i = 0; i < k; ++i) { inv[an[i]] = i; }
  vector<int> dp(m + 1, INF);
  auto fx = [](int x, int y) { return max(x, y); };
  auto ef = [] { return 0; };
  algo::segtree seg(k, INF, fx, ef);
  int ans = INF;
  int sm = algo::sqrt(m);
  for (int i = m; i > sm; --i) {
    dp[i] = i;
    if (inv[i] != -1) { seg.set(inv[i], dp[i]); }
    ans = min(ans, seg.query() - i);
  }
  for (int i = sm; i > 0; --i) {
    dp[i] = i;
    if (inv[i] != -1) { seg.set(inv[i], dp[i]); }
    for (int j = i * i; j <= m; j += i) {
      dp[j] = min(dp[j], dp[j / i]);
      if (inv[j] != -1) { seg.set(inv[j], dp[j]); }
    }
    ans = min(ans, seg.query() - i);
  }
  cout << ans << '\n';
}

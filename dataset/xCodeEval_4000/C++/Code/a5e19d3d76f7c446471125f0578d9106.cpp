using namespace std;
#define endl "\n"

int main() {
    ios::sync_with_stdio(0), cin.tie(NULL), cout.tie(NULL);
    int t; cin >> t;
    while(t--) {
        int n; cin >> n;
        bool found = false;
        string s,r;
        set<string> st2, st3;
        for(int i=0; i<n; i++) {
            cin >> s;
            if(found == true) continue;
            if(s.size() == 1) found = true;
            else if(s.size() == 2) found = (s[0] == s[1]);
            else found = (s[0] == s[2]);

            if(found == true) continue;
            r = s;
            reverse(r.begin(),r.end());

            if(s.size() == 2) {
                if(st2.find(r) != st2.end()) {
                    found = true;
                    continue;
                }
                if(st3.find(r) != st3.end()) {
                    found = true;
                    continue;
                }
                st2.insert(s);
            }
            else {
                if(st2.find(r.substr(0,2)) != st2.end()) {
                    found = true;
                    continue;
                }
                if(st3.find(r) != st3.end()) {
                    found = true;
                    continue;
                }
                    st3.insert(s);
                    st3.insert(s.substr(0,2));
            }
        }
        if(found == true) cout << "YES" << endl;
        else cout << "NO" << endl;
    }
    return 0;
}

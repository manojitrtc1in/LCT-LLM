#define ll long long
using namespace std;
const int Mod=1e9+7;
const int M=2*1e5+1;
ll f[M][10];
ll ans[10];
int t,n;
int get()
{
    char ch=getchar();
    int x=0;
    while(ch<'0'||ch>'9')
    {
        ch=getchar();
    }
    while(ch>='0'&&ch<='9')
    {
        int nw=ch-'0';
        ans[nw]++;
        ch=getchar();
    }
    while(ch<'0'||ch>'9')
    {
        ch=getchar();
    }
    while(ch>='0'&&ch<='9')
    {
        x=(x<<1)+(x<<3)+(ch^48);
        ch=getchar();
    }
    return x;
}
ll dfs(int nw,int p)
{
    

    if(p==0)return 1;
    ll ansl=0;
    if(nw==9)
    {
        if(f[p-1][1]!=0)
        {
            ansl+=f[p-1][1];
            ansl%=Mod;
        }
        else
        {
            ansl+=dfs(1,p-1);
            ansl%=Mod;
        }
        if(f[p-1][0]!=0)
        {
            ansl+=f[p-1][0];
            ansl%=Mod;
        }
        else
        {ansl+=dfs(0,p-1);
            ansl%=Mod;
        }
    }
    else
    {
        if(f[p-1][nw+1]!=0)
        {
            ansl+=f[p-1][nw+1];
            ansl%=Mod;
        }
        else
        {
            ansl+=dfs(nw+1,p-1);
            ansl%=Mod;
        }
    }
    f[p][nw]=ansl;
    return ansl;
}
int main()
{
   

    cin>>t;
    while(t--)
    {
        ll sum=0;
        memset(ans,0,sizeof ans);
        n=get();
        for(int i=0;i<10;i++)
        {
            sum+=(ans[i]*dfs(i,n));
            sum%=Mod;
        }
        printf("%lld\n",sum);
    }
}

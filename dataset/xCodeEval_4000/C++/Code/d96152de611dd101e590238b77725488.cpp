int main() {




    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll t;
    cin>>t;
    while(t--){
        ll n ;
        cin>>n;
        vector<ll> arr(n);
        vector<ll> b(n);
        for(int i = 0 ; i < n ; i ++){
            cin>>arr[i]>>b[i];
        }
        vector<vector<ll>> dp(n,vector<ll>(3,0));
        dp[0][1] = b[0];
        dp[0][2] = 2*b[0];
        for(int i = 1; i < n ; i++){
            for(int j = 0 ; j <3; j ++){
                dp[i][j] = 1e15;
                for(int z = 0 ; z < 3; z++){
                    if(arr[i-1]+z!=arr[i]+j){
                        dp[i][j] = min(dp[i][j],dp[i-1][z]);
                    }
                }
                dp[i][j]+=j*b[i];
            }
        }
        cout<<min(dp[n-1][0],min(dp[n-1][1],dp[n-1][2]))<<"\n";
    }
    return 0;
}
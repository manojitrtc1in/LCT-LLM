bool good(int n) {
	if (n <= 1) return false;
	bool found = false;
	for (int i = 1; i * i <= n; i++) {
		if (i != 1 && n % i == 0) return false;
		if (!found) {
			int t = (int)(sqrt(n - i * i + 0.) + 1e-9);
			if (t * t == n - i * i) {
				found = true;
			}
		}
	}	
	return found;
}


#include<algorithm>
#include<cmath>
#define max 2000000+10
using namespace std;
struct time{
	int start;
	int end;
}c[max],p[max];
bool cmp1(const struct time a,const struct time b){
		return a.start<b.start;
}
bool cmp2(const struct time a,const struct time b){
	return a.end<b.end;
}
int main(){
	int n;cin>>n;
	for(int i=0;i<n;i++)cin>>c[i].start>>c[i].end;
	int m;cin>>m;
	for(int i=0;i<m;i++)cin>>p[i].start>>p[i].end;
	sort(c,c+n,cmp1);
	sort(p,p+m,cmp1);
    struct time c1,p1;
    c1.start=c[n-1].start;p1.start=p[m-1].start;
    sort(c,c+n,cmp2);
    sort(p,p+m,cmp2);
    c1.end=c[0].end;p1.end=p[0].end;
    int periods;
    periods=c1.start-p1.end>p1.start-c1.end?c1.start-p1.end:p1.start-c1.end;
    if(periods>0)cout<<periods<<endl;
    else cout<<"0"<<endl;
}








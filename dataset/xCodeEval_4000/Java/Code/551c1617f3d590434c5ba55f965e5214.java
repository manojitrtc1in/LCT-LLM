    static class TaskB {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            try {
                int n = in.readInt();
                int[] a = in.readIntArray(n);
                int[] from = new int[n - 1], to = new int[n - 1];
                in.readIntArrays(from, to);
                MiscUtils.decreaseByOne(from, to);
                Graph graph = BidirectionalGraph.createGraph(n, from, to);
                long total = new IntArray(a).sum();
                long[] subtree = new long[n];
                long rem = dfs(graph, 0, a, subtree);
                if (rem != 0) {
                    rem *= dfs(graph, 0, a, rem);
                }
                long res = total - rem;
                out.printLine(res);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        int dfs(Graph graph, int u, int[] a, long min) {
            int nchildren = CollectionUtils.asList(graph.outbound(u)).size();
            if (nchildren == 0) {
                return (int) (a[u] / min);
            }
            min /= nchildren;
            int res = Integer.MAX_VALUE;
            for (Edge ed : graph.outbound(u)) {
                int v = ed.getDestination();
                res = Math.min(res, dfs(graph, v, a, min));
            }
            return res;
        }

        long dfs(Graph graph, int u, int[] a, long[] subtree) {
            subtree[u] = a[u];
            long lcm = 1;
            int size = 0;
            for (int ed = graph.firstOutbound(u); ed != -1; ed = graph.nextOutbound(ed)) {
                int v = graph.destination(ed);
                graph.removeEdge(graph.transposed(ed));
                long x = dfs(graph, v, a, subtree);
                if (x == 0) {
                    return 0;
                }
                subtree[u] += subtree[v];
                lcm = IntegerUtils.lcm(lcm, x);
                if (lcm > subtree[u]) {
                    return 0;
                }
                size++;
            }
            long res = lcm * Math.max(size, 1);
            return res > subtree[u] ? 0 : res;
        }

    }


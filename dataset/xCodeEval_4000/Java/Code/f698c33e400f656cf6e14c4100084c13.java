	static long pow(long a, int k, long p) {
		long m = k;
		long ans = 1;

		

		long curr = a;

		

		while(m > 0) {
			if( (m&1) == 1 ) {
				ans *= curr;
				ans %= p;
			}
			m >>= 1;
			curr *= curr;
			curr %= p;
		}
		return ans;		
	}

	

	static long inverse(long a, long p) {
		return pow(a, (int)(p-2), p);
	}
	
	class FenwickTree{
		int[] tree;
		int max;
		public FenwickTree(int max) {
			max++;
	
			this.max = (Integer.highestOneBit(max)<<1)-1;
			
			tree = new int[this.max];
		}

		private void add(int index, int val) {
			index++;
			while(index < max) {
				tree[index] += val;
				index += index&(-index);
			}
		}
		public int query(int l, int r) {
			return query(r)-query(l-1);
		}
		
		private int query(int index) {
			index++;
			
			int ans = 0;
			while(index > 0) {
				ans += tree[index];
				index -= index&(-index);
			}
			return ans;
		}
	}


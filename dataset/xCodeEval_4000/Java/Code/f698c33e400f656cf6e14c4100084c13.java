	private int solve(int[] s, int[] t) {
		final int n = s.length;
		final int m = t.length;
		

		
		

		

		

		
		final int MAX = 200000;
		
		this.n = s.length;
		this.t = t;
		
		int[] cnt = new int[MAX+1];
		for(int i=0; i<n; i++)
			cnt[s[i]]++;
		
		cntTree = new FenwickTree(MAX);
		for(int i=0; i<n; i++)
			cntTree.add(s[i], 1);
		
		fac = new long[MAX+1];
		fac[0] = 1;
		for(int i=1; i<=MAX; i++)
			fac[i] = fac[i-1]*i % MOD;
		
		permutateAndSort(s);
		
		long combination = fac[n];
		for(int i=1; i<=MAX; i++)
			if(cnt[i] > 0)
				combination = combination*inverse(fac[cnt[i]], MOD)%MOD;
		
		long ans = rec(0, combination);
		
		return (int)ans;
	}
	
	private long rec(int index, long combination) {
		int m = t.length;
		long ans = 0;
		if(index == n)
			return 1;
		
		

		

		


		int numLess = cntTree.query(1, t[index]-1);
		if(numLess > 0) {
			

			

			long temp = combination*numLess%MOD;
			temp = temp*inverse(n-index, MOD)%MOD;
			ans += temp;
		}
		
		int numEq = cntTree.query(t[index], t[index]); 

		if(numEq > 0) {
			if(index+1 < m) {
				cntTree.add(t[index], -1);
				long combinationNext = combination*inverse(n-index, MOD)%MOD;
				combinationNext = combinationNext*numEq%MOD;
				ans += rec(index+1, combinationNext);
			}
		}
		
		return ans % MOD;
	}
	

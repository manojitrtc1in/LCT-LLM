    static class TaskD {
        private int n;
        private int[][] g;
        private int[] level;
        private int realResult = -1;

        public void solve(int testNumber, InputReader in, OutputWriter out) {
            input(in);
            if (realResult != -1) {
                out.print(realResult, 0);
                return;
            }

            initLevels();

            int maxLevel = ArrayUtils.maxElement(level);
            int centerCount = 0;
            int minIndex = Integer.MAX_VALUE;
            for (int v = 0; v < n; v++) {
                if (level[v] == maxLevel) {
                    centerCount++;
                    minIndex = Math.min(minIndex, findMinIndex(v, -1));
                }
            }

            if (maxLevel == 0) {
                for (int v = 0; v < n; v++) {
                    if (g[v].length > 0) {
                        out.print(v + 1, 1);
                        return;
                    }
                }
            }

            int edgesCount = getEdgesCount();
            int pathToRemove = maxLevel * 2 + centerCount - 1;
            int result = edgesCount - pathToRemove;
            out.print(minIndex + 1, result);
        }

        private int getEdgesCount() {
            int cnt = 0;
            for (int[] a : g) {
                cnt += a.length;
            }
            return cnt;
        }

        private int findMinIndex(int cur, int p) {
            if (level[cur] == 0) {
                return cur;
            }
            int minIndex = Integer.MAX_VALUE;
            for (int to : g[cur]) {
                if (to != p && level[to] + 1 == level[cur]) {
                    minIndex = Math.min(minIndex, findMinIndex(to, cur));
                }
            }
            return minIndex;
        }

        private void initLevels() {
            int[] deg = new int[n];
            for (int v = 0; v < n; v++) {
                deg[v] = g[v].length;
            }
            IntQueue q = new IntArrayQueue();
            level = new int[n];
            for (int v = 0; v < n; v++) {
                if (deg[v] == 1) {
                    q.add(v);
                }
            }
            while (!q.isEmpty()) {
                int cur = q.poll();
                for (int p : g[cur]) {
                    if (deg[p] > 0) {
                        if (--deg[p] == 1) {
                            q.add(p);
                            level[p] = level[cur] + 1;
                        }
                    }
                }
            }
        }

        private void input(InputReader in) {
            n = in.readInt();
            int m = in.readInt();
            IntSet[] graph = inputGraph(in, n);

            if (m == 1) {
                realResult = in.readInt();
                return;
            }

            boolean[] needUse = new boolean[n];
            for (int i = 0; i < m; i++) {
                needUse[in.readInt() - 1] = true;
            }
            removeUseless(n, graph, needUse);
            g = compressGraph(graph);
        }

        private IntSet[] inputGraph(InputReader in, int n) {
            IntSet[] g = new IntSet[n];
            for (int i = 0; i < n; i++) {
                g[i] = new IntHashSet();
            }
            for (int v = 0; v < n - 1; v++) {
                int a = in.readInt() - 1;
                int b = in.readInt() - 1;
                g[a].add(b);
                g[b].add(a);
            }
            return g;
        }

        private void removeUseless(int n, IntSet[] g, boolean[] needUse) {
            IntQueue q = new IntArrayQueue();
            for (int v = 0; v < n; v++) {
                if (!needUse[v] & g[v].size() == 1) {
                    q.add(v);
                }
            }
            while (!q.isEmpty()) {
                int cur = q.poll();
                int p = g[cur].first();
                g[cur].remove(p);
                g[p].remove(cur);
                if (!needUse[p] && g[p].size() == 1) {
                    q.add(p);
                }
            }
        }

        private int[][] compressGraph(IntSet[] graph) {
            int[][] g = new int[graph.length][];
            for (int v = 0; v < graph.length; v++) {
                g[v] = graph[v].toArray();
            }
            return g;
        }

    }


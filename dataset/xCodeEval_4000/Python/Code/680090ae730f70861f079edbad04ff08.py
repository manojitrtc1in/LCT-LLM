n = int(input())
for _ in range(n):
    n, k = list(map(int, input().split()))
    mountains = list(map(int, input().split()))
    for i in range(k):
        ans = -1
        for j in range(n - 1):
            if mountains[j] < mountains[j + 1]:
                ans = j
                mountains[j] += 1
                break
        if ans == -1:
            break
    print(ans if ans == -1 else ans+1)

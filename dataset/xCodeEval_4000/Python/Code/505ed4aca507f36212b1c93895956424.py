for _ in range(t):
    a, b, c, n = map(int, input().split())
    
    
    
    
    m = max(a,b,c)
    d = n-(sum([abs(m-a),abs(b-m),abs(c-m)]))
    print("YES" if(d>=0 and d%3==0) else "NO")
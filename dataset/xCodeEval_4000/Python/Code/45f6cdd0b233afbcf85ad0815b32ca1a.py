m, n = map(int, input().split())
g = [input() for i in range(m)]
ans = 'YES'
if m < 3 and n < 3:
    ans = 'NO'
if ans == 'YES':
    c = (-1, -1)
    for i in range(1, m - 1):
        for j in range(1, n - 1):
            if g[i][j] == '*':
                if all(g[I][J] == '*' for I, J in ((i + 1, j), (i - 1, j), (i, j + 1), (i, j - 1))):
                    c = (i, j)
                    break
    if c == (-1, -1):
        ans = 'NO'

if ans == 'YES':
    plus = {c}
    I, J = c
    i, j = I - 1, I + 1
    while i >= 0 and g[i][J] == '*':
        plus.add((i, J))
        i -= 1
    while j < m and g[j][J] == '*':
        plus.add((j, J))
        j += 1
    i, j = J - 1, J + 1
    while i >= 0 and g[I][i] == '*':
        plus.add((I, i))
        i -= 1
    while j < n and g[I][j] == '*':
        plus.add((I, j))
        j += 1

    for i in range(m):
        for j in range(n):
            if g[i][j] == '*' and (i, j) not in plus:
                ans = 'NO'
                break

print(ans)

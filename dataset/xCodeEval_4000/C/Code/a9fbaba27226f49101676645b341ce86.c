int main() {
    int count_input_data;
    scanf("%d", &count_input_data);

    for(int i = 0;i<count_input_data;i++) {
        int weight, height, necessary_count_lists;
        scanf("%d %d %d", &weight, &height, &necessary_count_lists);

        int check_weight = weight % 2 != 0;
        int check_height = height % 2 != 0;
        int count_lists = 1;

        while (check_weight == 0 || check_height == 0) {
            if (check_weight == 0) {
                weight /= 2;
                check_weight = weight % 2 != 0;
                count_lists *= 2;

            } else {
                height /= 2;
                check_height = height % 2 != 0;
                count_lists *= 2;
            }
        }

        if (count_lists >= necessary_count_lists) {
            printf("Yes\n");

        } else {
            printf("No\n");
        }
    }

    return 0;
}




























int main() {
    int count_sets;
    scanf("%d", &count_sets);

    int count_odd = 0;
    int count_even = 0;

    for (int i = 0; i < count_sets; i++) {
        int count_pars;
        scanf("%d", &count_pars);

        int count_numbers = count_pars * 2;

        for (int j = 0; j < count_numbers; j++) {
            int numbers;
            scanf("%d", &numbers);

            if (numbers % 2 == 0) {
                count_even++;
            } else {
                count_odd++;
            }
        }

        if (count_even == count_odd) {
            printf("YES\n");

        } else {
            printf("NO\n");
        }

        count_odd = count_even = 0;
    }

    return 0;
}

























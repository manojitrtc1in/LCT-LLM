void sort(int a[], int n) {
	for (int i=0; i<n; i++) {
		for (int j=n-2; j>=i; j--) {
			if (a[j]>a[j+1]) {
				int temp = a[j];
				a[j] = a[j+1];
				a[j+1] = temp;
			}
		}
	}
}

int main () {
	int T;
	scanf ("%d",&T);
	for (int i=0; i<T; i++) {
		int n,l,r,k;
		int count=0;
		scanf ("%d %d %d %d",&n ,&l ,&r, &k);
		int a[n+5];
		for (int j=0; j<n; j++) {
			scanf ("%d",&a[j]);
		}
		sort(a,n);
		for (int j=0; j<n; j++) {
			if (a[j]<=r && a[j]>=l && k>=a[j]) {
				k -= a[j];
				count++;
			}
		}
		printf ("%d\n",count);
	}
	return 0;
}








import string
alpha = string.ascii_lowercase
for t in range(int(input())):
    s = input()
    a = [ord(ch) - ord('a') for ch in s]
    a.sort()
    odd, even = [], []
    for ch in a:
        if ch % 2:
            odd.append(ch)
        else:
            even.append(ch)
    if odd and even:
        b = odd + even if abs(odd[-1] - even[0]) > 1 else even + odd
    else:
        b = (odd, even)[not len(odd)]
    ans = ''
    for i in range(1, len(b)):
        if abs(b[i] - b[i - 1]) == 1:
            ans = 'No answer'
            break
    if ans == '':
        for i in range(len(b)):
            ans += alpha[b[i]]
    print(ans)

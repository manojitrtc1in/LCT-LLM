n, m, k = cin()
a = list(cin())
b = sorted(a, reverse=True)
dic = {}
for i in range(m * k):
    dic[b[i]] = dic.get(b[i], 0) + 1
ans = [0]
for i in range(k - 1):
    cnt = 0
    j = ans[-1]
    while cnt < m:
        if a[j] in dic:
            cnt += 1
            dic[a[j]] -= 1
            if not dic[a[j]]:
                del dic[a[j]]
        j += 1
    ans.append(j)

n, m = map(int, input().split())
a = [list(map(int, input().split())) for i in range(m)]
x, y = a.pop()
ans = 'NO'
for v in (x, y):
    val = [0] * (n + 1)
    cnt = 0
    for v1, v2 in a:
        if v1 != v and v2 != v:
            val[v1], val[v2], cnt = val[v1] + 1, val[v2] + 1, cnt + 1
    if max(val) == cnt:
        ans = 'YES'
        break
print(ans)

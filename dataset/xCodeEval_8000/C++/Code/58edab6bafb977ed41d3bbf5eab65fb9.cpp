#include <vector>
#include <algorithm>
#include <set>
#include <cmath>
#include <map>
#include <iomanip>
#include <sstream>
using namespace std;
const int MAXN = 1e5 + 10;
int ar [ MAXN ], m , n , ar2 [ MAXN ] , last [ MAXN ] , ar3 [ MAXN ];
bool flag [ MAXN ] , flag2;
long long sum;
int main() {
	ios::sync_with_stdio(false) , cin.tie(0) , cout.tie(0);
	cin >> n >> m;
	sum = m;
	for(int i = 1 ; i <= n ; i++)
		cin >> ar [ i ];
	for(int i = 1 ; i <= n ; i++)
	{
		if(!flag [ ar [ i ] ] && ar [ i ] != 0)
		{
			flag [ ar [ i ] ] = true;
			ar2 [ i ] = ar2 [ i - 1 ] + 1;
		}
		else
			ar2 [ i ] = ar2 [ i - 1 ];
	}		
	for(int i = 1 ; i <= n ; i++)
		last [ ar [ i ] ] = i;
	for(int i = 0 ; i < m ; i++)
	{
		cin >> ar3 [ i ];
		sum += ar3 [ i ];
	}	
	for(int i = 1 ; i <= m ; i++)
		if(last [ ar [ i ] ] - 1 < ar3 [ ar [ i ] ])
			return cout << -1 , 0; 	
	for(int i = sum ; i <= n ; i++)
		if(ar [ i ] || flag2)
		{
			flag2 = true;
			if(ar2 [ i ] == m)
				return cout << i, 0;
		}		
	cout << -1;		
			
}
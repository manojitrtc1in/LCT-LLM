int n[MAXN], ans[MAXN];

inline int lowbit(int x) { return x & -x; }

inline int sum(int p)
{
    int ret = 0;
    while (p > 0)
    {
        ret += n[p];
        p -= lowbit(p);
    }
    return ret;
}

inline void add(int p, int v)
{
    while (p < MAXN)
    {
        n[p] += v;
        p += lowbit(p);
    }
}

inline void update(int l, int r, int v)
{
    int ls = sum(l - 1), rs = sum(r);
    if (rs == ls) return;
    if (l == r)
    {
        add(l, -1);
        ans[l] = v;
        return;
    }
    int m = (l + r) >> 1;
    update(l, m, v);
    update(m + 1, r, v);
}


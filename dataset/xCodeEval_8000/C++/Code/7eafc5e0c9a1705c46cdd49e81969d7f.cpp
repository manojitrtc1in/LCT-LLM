long double y [ MAXN ];
int ar [ MAXN ];
int main() {
	ios::sync_with_stdio(false) , cin.tie(0) , cout.tie(0);
	int n , r;
	cin >> n >> r;
	for(int i = 0 ; i < n ; i++)
		cin >> ar [ i ];
	for(int i = 0 ; i < n ; i++)
	{
		long double a = -1 , b = -1;
		for(int j = i - 1 ; j >= 0 ; j--)
			if(abs(ar [ j ] - ar [ i ]) <= 2 * r  )
			{
				a = y [ j ]; 
				b = ar [ j ];
				y [ i ] = max( y [ i ] , (sqrt(pow(2 * r , 2) - pow(abs(b - ar [ i ]) , 2))) + a);
			}
		if(a == -1)
			y[ i ]	= r;
	}
	for(int i = 0 ; i < n ; i++)
		cout << fixed<<setprecision(7)<< y [ i ] << ' ';
}
long long a[5005],b[5005];
long long dp[5005][5005];
long long n;
long long ans = 0;
long long solve(long long l,long long r){
    if(l>r)return 0;
    if(l == r)return a[l]*b[r];
    if(dp[l][r] != -1)return dp[l][r];
    
    return dp[l][r] = a[l]*b[r] + a[r]*b[l] + solve(l+1,r-1);
}
int main(){
    	ios_base::sync_with_stdio(0);
    cin.tie(0);cout.tie(0);
    cin>>n;
    for(long long i=0;i<n;i++){
        cin>>a[i];
    }

    for(long long i=0;i<n;i++){
        cin>>b[i];
        ans+=(a[i]*b[i]);
    }
    long long prefix[n];
    prefix[0] = a[0]*b[0];
    
    for(long long i=1;i<n;i++){
        prefix[i] = a[i]*b[i] + prefix[i-1];
    }
    memset(dp,-1,sizeof(dp));
    for(long long i=0;i<n;i++){
        for(long long j = i;j<n;j++){
            if(dp[i][j] == -1){
                solve(i,j);
            }
            ll temp = prefix[n-1]-prefix[j];
            if(i > 0){
                temp+=prefix[i-1];
            }
            temp+=dp[i][j];
            ans = max(ans,temp);
        }
    }
    cout<<ans<<endl;
    return 0;
}
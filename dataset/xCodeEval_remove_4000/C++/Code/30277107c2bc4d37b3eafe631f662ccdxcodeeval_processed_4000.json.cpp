struct Ptr{
	int a,b,whr;
	Ptr(int a,int b,int whr):a(a),b(b),whr(whr){}
	Ptr(){}
	bool operator<(const Ptr& A)const{
		return b<A.b||(b==A.b&&a>A.a);
	}
}a[100005];


int chs[MASK(21)];
vector<int> hv;
int n,m;
char s[25][25];
int a[25][25];
int dp[MASK(21)];

int main(){
	fio>>n>>m;
	for(int i=0;i<n;i++){
		fio>>s[i];
	}
	for(int i=0;i<n;i++){
		for(int j=0;j<m;j++){
			fio>>a[i][j];
		}
	}
	FILLBIG(chs);
	for(int i=0;i<n;i++){
		hv.pb(MASK(i));
		for(int j=0;j<m;j++){
			chs[MASK(i)]=min(chs[MASK(i)],a[i][j]);
		}
	}
	for(int i=0;i<m;i++){
		for(int j=0;j<26;j++){
			int num=0,maxx=0,sum=0;
			for(int k=0;k<n;k++){
				if(s[k][i]==j+'a'){
					num|=MASK(k);
					maxx=max(maxx,a[k][i]);
					sum+=a[k][i];
				}
			}
			if(!num){
				continue;
			}
			if(chs[num]==Inf){
				hv.pb(num);
			}
			chs[num]=min(chs[num],sum-maxx);
		}
	}
	FILLBIG(dp);
	dp[0]=0;
	for(int i=0;i<MASK(n)-1;i++){
		for(int j=0;j<SZ(hv);j++){
			dp[i|hv[j]]=min(dp[i|hv[j]],dp[i]+chs[hv[j]]);
		}
	}
	fio<<dp[MASK(n)-1]<<endl;
	return 0;
}
bool f(long long arr[],long long n,long long tot,long long k){
   

    long long c = 0;
    long long val =0 ;
    for (long long i=tot-2;c<k-1;i--){
        val +=(n-arr[i]);
        c++;
    }
   

   

    return (val<arr[tot-1]);
}



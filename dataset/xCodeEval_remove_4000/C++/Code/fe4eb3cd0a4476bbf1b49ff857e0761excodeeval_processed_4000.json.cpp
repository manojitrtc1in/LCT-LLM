const int N = 26;
pair<int,char> cnt[N];
bool used[N];
bool dead[N];
int n,k;string s;
int main(){
    cin>>s;
    int n = s.length();
    scanf("%d",&k);
    for(int i=0;i<26;++i){
        cnt[i].se = i;
    }
    for(int i=0;i<n;++i){
        cnt[s[i]-'a'].fi++;
    }
    sort(cnt,cnt+26);
    for(int i=0;i<26 && k>0;++i){
        if(cnt[i].fi>k)break;
        k -= cnt[i].fi;
        dead[cnt[i].se] = 1;
    }
    int dif = 0;
    string ans = "";
    for(int i=0;i<s.length();++i){
        if(!dead[s[i]-'a']){
            ans += s[i];
        }
    }
    for(int i=0;i<ans.length();++i){
        if(!used[ans[i]-'a']){
            used[ans[i]-'a'] = 1;
            dif++;
        }
    }
    cout<<dif<<"\n"<<ans<<"\n";
    return 0;
}






















































































































































































































        












        








 



































        
































































































































































































































































































































































































































































    
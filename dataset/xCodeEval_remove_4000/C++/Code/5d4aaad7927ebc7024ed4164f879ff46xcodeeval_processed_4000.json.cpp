int naive(istream& cin, ostream& cout) {
  ll n; cin >> n;
  string s; cin >> s;

  auto func = [&](ll l, ll r) -> ll {
    string t = s.substr(l, r - l);
    ll ret = 0;
    ll c = 0;
    for (ll i = 0; i < (ll)t.size(); i++) {
      if (t[i] == '0') {
	ret = max(ret, c);
	c = 0;
      }else {
	c++;
      }
    }
    ret = max(ret, c);
    

    return ret;
  };

  ll ans = 0;
  for (ll l = 0; l < n; l++) {
    for (ll r = l + 1; r <= n; r++) {
      ans += func(l, r);
    }
  }
  cout << ans << endl;

  return 0;
}
int body(istream& cin, ostream& cout) {

  ll n; cin >> n;
  string s; cin >> s;
  ll ans = 0;
  ll cur = 0;
  ll streak = 0;

  using DAT = ll;
  using OP = ll;
  const DAT unit_dat = 0;
  const OP unit_op = 0;
  auto xAdd = [](DAT x, DAT y) -> DAT { return x + y; };
  auto id3 = [](int k, OP f, DAT x) -> DAT { return k * f + x; };
  auto xComp = [](OP h, OP g) -> OP { return h + g; };
  auto st = id0(unit_dat, unit_op, xAdd, xComp, id3, true);
  
  vector<ll> init(n + 1, n);
  st.set_data(init);

  for (ll i = n - 1; i >= 0; i--) {
    if (s[i] == '0') {
      ans += cur;
      streak = 0;
      continue;
    }
    streak++;
    ll m = st.query(streak, streak + 1) - i;
    cur += m;
    ans += cur;
    st.update(1, streak, -1);
    st.update(streak, streak + 1, i + streak - 1 - (m + i));
    

  }
  cout << ans << endl;

  return 0;
}


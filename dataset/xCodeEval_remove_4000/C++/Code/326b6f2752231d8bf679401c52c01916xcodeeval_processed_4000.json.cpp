li dp[N];

void solve() {
	li pr = accumulate(a, a + n, 0ll);
	dp[n] = 0;
	li mx = -INF64;
	for (int i = n - 1; i >= 0; --i){
		mx = max(mx, -dp[i + 1] + pr);
		pr -= a[i];
		dp[i] = mx;
	}
	

	printf("%lld\n", dp[1]);
}


vector<pair<ll,ll>> canon(ll p) {
    vector<pair<ll,ll>> answer;
    for (ll i = 2; i * i <= p; i++) {
        if (p % i == 0) {
            ll k =0;
            while (p % i == 0) {
                p /= i;
                k++;
            }
            answer.push_back({i,k});
        }
    }
    if (p > 1)answer.push_back({p,1});
    return answer;
}









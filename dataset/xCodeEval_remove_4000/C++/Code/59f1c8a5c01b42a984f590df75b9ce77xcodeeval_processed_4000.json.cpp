int main() {




    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll t;
    cin>>t;
    while(t--){
        ll n;
        cin>>n;
        vector<ll> arr(n);
        cin>>arr;
        if(n == 1){
            cout<<arr<<"\n";
            continue;
        }
        sort(all(arr));
        vector<ll> res(n);
        for(int i =0 ; i < n/2; i ++){
            res[2*i] = arr[n-1-i];
            res[2*i+1] = arr[i];
        }
        if(n&1){
            res[n-1] = arr[n/2];
        }
        reverse(all(res));
        bool b = false;
        for(int i = 2; i < n &&!b; i ++){
            if(abs(res[i]-res[i-1]) < abs(res[i-1]-res[i-2])){
                b = true;
            }
        }
        if(b){
            vector<ll> res(n);
            for(int i =0 ; i < n/2; i ++){
                res[2*i] = arr[i];
                res[2*i+1] = arr[n-1-i];
            }
            if(n&1){
                res[n-1] = arr[n/2];
            }
            reverse(all(res));
            cout<<res<<"\n";
        }else{
            cout<<res<<"\n";
        }


    }
    return 0;
}


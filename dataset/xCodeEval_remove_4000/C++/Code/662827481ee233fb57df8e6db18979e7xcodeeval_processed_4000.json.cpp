mint solve(const vector<int>& p) {
  const int n = p.size();

  

  

  

  

  


  vector<mint> id6(n);
  id10<mint> id13(n);
  for (int i = 0; i < n; ++i) {
    id13.add(p[i], id13.sum(0, p[i]) + 1);
    id6[i] = id13.sum(p[i], p[i] + 1);
  }

  vector<mint> id2(n);
  id10<mint> id1(n);
  for (int i = n - 1; i >= 0; --i) {
    id1.add(p[i], id1.sum(p[i], n) + 1);
    id2[i] = id1.sum(p[i], p[i] + 1);
  }

  vector<int> suffix_max(n);
  suffix_max[n - 1] = p[n - 1];
  for (int i = n - 2; i >= 0; --i) {
    suffix_max[i] = max(suffix_max[i + 1], p[i]);
  }

  vector<int> inv(n);
  for (int i = 0; i < n; ++i) {
    inv[p[i]] = i;
  }

  vector<mint> id3(n);
  id3[inv[n - 1]] = 1;
  id10<mint> id9(n);
  id9.add(inv[n - 1], 1);

  int paired = n - 1;
  for (int last = n - 2; last >= 0; --last) {
    if (suffix_max[inv[last]] == last) {
      for (int first = paired - 1; first > last; --first) {
        id9.add(inv[first], id9.sum(inv[first], inv[paired] + 1));        
        id3[inv[first]] = id9.sum(inv[first], inv[first] + 1);
      }

      for (int first = paired; first > last; --first) {
        id9.add(inv[first], -id9.sum(inv[first], inv[first] + 1));
      }

      id3[inv[last]] = 1;
      id9.add(inv[last], 1);
      paired = last;
    }
  }

  for (int first = paired - 1; first >= 0; --first) {  

    id9.add(inv[first], id9.sum(inv[first], inv[paired] + 1));        
    id3[inv[first]] = id9.sum(inv[first], inv[first] + 1);
  }

  

  

  

  

  


  mint ans = 0;
  for (int i = 0; i < n; ++i) {
    ans += (id2[i] - id3[i]) * id6[i];
  }
  return ans;
}


vector<string> splitString(string si)
{
    vector<string> s;
    string k = "";
    for(int i=0; i<(int)si.length();i++)
    {
        if(si[i]==' ' && k.length()>=1)
        {
            s.push_back(k);
            k = "";
        }
        else if(si[i] != ' ')
        {
            k = k+si[i];
        }
    }
    if(k!="")
        s.push_back(k);
    return s;
}
 
 

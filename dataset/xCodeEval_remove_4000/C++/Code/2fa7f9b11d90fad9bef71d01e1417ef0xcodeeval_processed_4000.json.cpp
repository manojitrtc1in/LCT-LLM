




using namespace std;






























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































int main()
{
	int n,m,i,tmp1,tmp2,col=0,row=0;
	long long sum;
	cin>>n>>m;
	vector<int> id1,id0,a(100010,1),b(100010,1);
	vector<long long> safe_num(100010,0);
	for (i=0;i<m;++i)
	{
		cin>>tmp1>>tmp2;
		id1.push_back(tmp1);
		id0.push_back(tmp2);
	}
	sum=(long long)n*n;
	for (i=0;i<m;++i)
	{
		if (a[id1[i]]==1)
		{
			sum=sum-n+col;
			a[id1[i]]=0;
			++row;
		}
		if (b[id0[i]]==1)
		{
			sum=sum-n+row;
			b[id0[i]]=0;
			++col;
		}
		safe_num[i]=sum;
	}
	for (i=0;i<m;++i)
	{
		cout<<safe_num[i]<<" ";
	}
	cout<<endl;
	return 0;
}

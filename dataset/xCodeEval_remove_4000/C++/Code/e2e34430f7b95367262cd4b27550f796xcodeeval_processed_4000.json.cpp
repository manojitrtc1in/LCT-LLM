int main(int argc, char const *argv[])
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> v(n + 1, 0);
        for (int i = 1; i <= n; i++)
        {
            int h;
            cin >> h;
            v[i] = v[i - 1] + h;
        }
        int ans = n;
        for (int i = 1; i < n; i++)
        {
            int h = i;
            int sum = i;
            if(v[n]%v[i]==0)
            {
                for (int j = i + 1; j <= n; j++)
            {
                
                    if (v[j] - v[h] == v[i])
                    {
                        sum = max(sum, j - h);
                        h = j;
                    }
             
            }
            
            if(h==n) ans = min(sum, ans);
    
            
        }}
        cout << ans << endl;
        

        

        

        

    }
    return 0;
}

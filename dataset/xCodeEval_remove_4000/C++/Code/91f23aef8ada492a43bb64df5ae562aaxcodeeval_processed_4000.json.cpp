char s[1000005];
int fail[1000005];
int bz[1000005][25];
int n,k;

int main(){
	fio>>n>>k;
	fio>>(s+1);
	int fu=0;
	fail[1]=0;
	for(int i=2;i<=n;i++){
		while(fu&&s[i]!=s[fu+1]){
			fu=fail[fu];
		}
		if(s[i]==s[fu+1]){
			fu++;
		}
		fail[i]=fu;
	}
	fail[0]=0;
	for(int i=0;i<=n;i++){
		bz[i][0]=fail[i];
	}
	for(int i=1;i<=20;i++){
		for(int j=0;j<=n;j++){
			bz[j][i]=bz[bz[j][i-1]][i-1];
		}
	}
	for(int i=1;i<=n;i++){
		int frm=(1LL*(k-1)*i+k-1)/k;
		int to=1LL*k*i/(k+1);
		int xx=i;
		for(int j=20;j>=0;j--){
			if(bz[xx][j]>to){
				xx=bz[xx][j];
			}
		}
		xx=bz[xx][0];
		if(frm<=xx&&to>=xx){
			fio<<1;
		}else{
			fio<<0;
		}
	}
	fio<<endl;
	return 0;
}
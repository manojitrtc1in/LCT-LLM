int main(int argc, char const *argv[])
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        unordered_map<int,vector<int>>p;
        for (int i = 0; i < n; i++)
        {
            int y;
            cin >> y;
            p[y].push_back(i + 1);
        }
        int ans = 0;
        for (auto q : p)
        {
            if (q.second.size() > 1)
            {
                ans = max(ans, q.second[q.second.size() - 2]);
            }
        }
        cout << ans << endl;
    }
    return 0;
}

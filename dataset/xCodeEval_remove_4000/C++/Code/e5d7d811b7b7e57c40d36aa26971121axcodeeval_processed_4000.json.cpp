int main()
{
	ll n;
	cin>>n;
	while(n--)
	{
		ll a,b,c,d;
		cin>>a>>b>>c>>d;
		if(b*d>=a)
		{
			cout<<-1<<'\n';
		}
		else if(b*c<a)
		{
			cout<<b<<'\n';
		}
		else if(b*c==a)cout<<b-1<<'\n';
		else 
		{
			long long ans=(a-b*d)/(c-d);
			if(b-ans<0)ans=b;
			while(a-ans*c-(b-ans)*d<=0){ans--;}
			cout<<ans<<'\n';
		}
	}
	return 0;
 } 




vector<int> a;
vector<vector<long long>> d;
vector<bool> used;
int n;
int tmp;
long long max(long long a, long long b);
void id0();
void Fill();
int main(void) {
	cin >> n;
	a = vector<int>(n+1);
	used = vector<bool>(n + 1,true);
	Fill();
	d = vector<vector<long long>>(n + 1, vector<long long>(n + 1, 0));
	id0();
	int answer = 0;
	
	for (int i = 0; i < n+1; i++)
	{
		if (used[i]==true && d[i][n]>=0) {
			answer = n-i;
			break;
		}
	}
	cout << answer << endl;

	return 0;
}
void id0() {
	for (int i = 1; i < n + 1; i++) {
		d[0][i] = d[0][i - 1] + a[i];
		if (d[0][i] < 0) {
			tmp = i;
			used[0] = false;
			break;
		}
	}
	for (int i = 1; i < n+1; i++) {
		for (int j = i+1; j < n + 1; j++) {
			if (j <= tmp) {
				d[i][j] = max(d[i][j - 1] + a[j], d[i - 1][j - 1]);
			}
			else {
				d[i][j] = d[i][j - 1] + a[j];
				if (d[i][j] < 0) {
					tmp = j;
					used[i] = false;
					break;
				}
			}
		}			
				
	}
}
long long max(long long a, long long b)
{
	if (a > b)
		return a;
	return b;
}
void Fill() {
	for (int i = 1; i < n + 1; i++) {
		cin >> a[i];
	}
}






























































































int a[1000], i, n, m, j, l, k, ans;

int main() {
    scanf("%d %d %d", &n, &m, &k);
    for (i = 0; i < m; i++)
        a[i] = 1000001;
    for (i = 0; i < n; i++) {
        scanf("%d %d", &j, &l);
        j--;
        a[j] = min(a[j], l);
    }
    for (i = 0; i < m; i++)
        ans += a[i];

    ans = min(ans, k);
    cout << ans << endl;

    return 0;
}

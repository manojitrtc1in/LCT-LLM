int n;
int a[200007];
vector<int> yi,er;

int main(){
	fio>>n;
	int cntf=0;
	for(int i=0;i<n;i++){
		fio>>a[i];
		if(!a[i]){
			yi.pb(i+1);
		}else if(a[i]<0){
			cntf++;
		}
		if(a[i]){
			er.pb(i+1);
		}
	}
	if(cntf%2){
		int maxf=-Inf,pos;
		for(int i=0;i<n;i++){
			if(a[i]<0&&a[i]>maxf){
				maxf=a[i];
				pos=i;
			}
		}
		pos++;
		er.erase(lower_bound(ALL(er),pos));
		yi.pb(pos);
	}
	if(SZ(yi)){
		for(int i=0;i<SZ(yi)-1;i++){
			fio<<"1 "<<yi[i]<<' '<<yi[i+1]<<endl;
		}
	}
	if(SZ(er)){
		if(SZ(yi)){
			fio<<"2 "<<yi[SZ(yi)-1]<<endl;
		}
		for(int i=0;i<SZ(er)-1;i++){
			fio<<"1 "<<er[i]<<' '<<er[i+1]<<endl;
		}
	}
	return 0;
}
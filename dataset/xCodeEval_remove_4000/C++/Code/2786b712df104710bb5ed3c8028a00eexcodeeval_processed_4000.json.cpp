struct factorial {
	std::vector<mint> fac, inv;

	factorial(int n) {
		fac.resize(n + 1);
		inv.resize(n + 1);
		fac[0] = inv[0] = 1;
		for (int i = 1; i <= n; i++) {
			fac[i] = fac[i - 1] * i;
			inv[i] = 1 / fac[i];
		}
	}

	mint operator[](int i) { return fac[i]; }

	mint operator()(int n, int k) { return n < k ? 0 : fac[n] * inv[n - k] * inv[k]; }
};

int main() {
	using namespace std;

	int N, K;
	cin >> N >> K;
	vector<vector<int>> T(N);
	for (int i = 1; i < N; i++) {
		int u, v;
		cin >> u >> v;
		u--, v--;
		T[u].push_back(v);
		T[v].push_back(u);
	}	

	factorial C(N);
	vector<int> siz(N);
	vector<mint> cnt(N);
	mint ans = 0, cur = 0;

	id6([&](auto self, int u = 0, int p = -1) -> void {
		siz[u] = 1;
		cnt[u] = 0;
		for (int v : T[u]) {
			if (v == p)
				continue;
			self(v, u);
			siz[u] += siz[v];
			cnt[u] -= C(siz[v], K);
		}
		cnt[u] += C(siz[u], K);
		cur += cnt[u] * siz[u];
	})();

	auto reroot = [&](int u, int v) {
		auto sizu = siz[u], sizv = siz[v];
		auto cntu = cnt[u], cntv = cnt[v];
		siz[u] = N - sizv;
		siz[v] = N;
		cnt[u] = cntu - C(sizu, K) + C(siz[u], K) + C(sizv, K);
		cnt[v] = cntv - C(sizv, K) + C(siz[v], K) - C(siz[u], K);
		cur -= sizu * cntu + sizv * cntv;
		cur += siz[u] * cnt[u] + siz[v] * cnt[v];
	};

	id6([&](auto self, int u = 0, int p = -1) -> void {
		ans += cur;
		for (int v : T[u]) {
			if (v == p)
				continue;
			reroot(u, v);
			self(v, u);
			reroot(v, u);
		}
	})();

	cout << ans << '\n';
}
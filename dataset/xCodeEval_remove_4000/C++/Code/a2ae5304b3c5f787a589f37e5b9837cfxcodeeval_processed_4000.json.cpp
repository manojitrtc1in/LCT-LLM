using namespace std;


























































































































int n,k;
ll a[200010];
multiset<ll>s;
int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        int flag=0;
        s.clear();
       scanf("%d%d",&n,&k);
       for(int i=1;i<=n;i++)
       {
           scanf("%lld",&a[i]);
           s.insert(a[i]);
       }
       for(auto it=s.begin();it!=s.end();it++)
       {
           if(s.find(*it+k)!=s.end()) flag=1;
           if(flag) break;
       }
       if(flag) printf("YES\n");
       else printf("NO\n");
    }
}

pair<Node*, Node*> split(Node* n, int k) {
	if (!n) return {};
	if (k<=0) return {nullptr, n};
	if (cnt(n->l) >= k) { 

		auto pa = split(n->l, k);
		n->l = pa.second;
		n->recalc();
		return {pa.first, n};
	} else {
		k -= cnt(n->l);
		if (n->val > k) {
			Node *oldl = n->l;
			n->l = nullptr;
			n->recalc();
			return {oldl, n};
		}
		else {
			auto pa = split(n->r, k - n->val); 

			n->r = pa.first;
			n->recalc();
			return {n, pa.second};
		}
	}
}

Node* merge(Node* l, Node* r) {
	if (!l) return r;
	if (!r) return l;
	if (l->y > r->y) {
		l->r = merge(l->r, r);
		l->recalc();
		return l;
	} else {
		r->l = merge(l, r->l);
		r->recalc();
		return r;
	}
}


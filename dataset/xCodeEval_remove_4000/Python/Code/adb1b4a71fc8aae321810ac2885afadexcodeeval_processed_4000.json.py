n = int(input())
a = list(map(int, input().split()))
a = {i: j for i, j in enumerate(a)}
m, idx = 0, 0
ans = 'YES'
for i in range(n):
    if a[i] > m:
        m = a[i]
        idx = i
for i in range(1, idx):
    if a[i] < a[i - 1]:
        ans = 'NO'
        break
for i in range(idx + 1, n):
    if a[i] > a[i - 1]:
        ans = 'NO'
        break
print(ans)

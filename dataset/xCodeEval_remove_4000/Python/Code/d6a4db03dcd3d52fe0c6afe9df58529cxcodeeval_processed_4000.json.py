import collections
n = int(input())
u = list(map(int, input().split()))
g, cnt = [0] * 10, 0
ans = 0
for i in range(n):
    g[u[i] - 1] += 1
    unique = set(g)
    unique.discard(0)
    if len(unique) == 1:
        k = unique.pop()
        if k == 1:
            ans = i
        elif g.count(k) == 1:
            ans = i
    elif len(unique) == 2:
        if 1 in unique and g.count(1) == 1:
            ans = i
        else:
            a, b = unique
            if a < b:
                b, a = a, b
            if a - b == 1 and g.count(a) == 1:
                ans = i
print(ans + 1)

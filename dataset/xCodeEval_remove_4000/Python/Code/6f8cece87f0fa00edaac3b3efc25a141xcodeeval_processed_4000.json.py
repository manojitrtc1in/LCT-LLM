for t in range(int(input())):
    n, m = map(int, input().split())
    grid = [input() for i in range(n)]
    rows = []
    cols = [0] * m
    for row in grid:
        rows.append(0)
        for i in range(m):
            if row[i] == '.':
                rows[-1] += 1
                cols[i] += 1
    ans = m + n - 1
    for i in range(n):
        for j in range(m):
            
            ans = min(ans, rows[i] + cols[j] - (grid[i][j] == '.'))
    print(ans)


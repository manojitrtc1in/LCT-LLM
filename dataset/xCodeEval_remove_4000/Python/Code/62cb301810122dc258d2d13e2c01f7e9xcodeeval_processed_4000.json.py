    i = 2
    while i < len(p):
        while len(q) >= 2 and opp(q[-2], q[-1], p[i]) <= 0:
            q.pop()
        q.append(p[i])
        i += 1
    q.extend((x[0], -x[1]) for x in q[1:-1])

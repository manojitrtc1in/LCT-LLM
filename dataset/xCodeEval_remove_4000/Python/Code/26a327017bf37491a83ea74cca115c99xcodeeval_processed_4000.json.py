def create_bits(num):
    st = ''
    while num > 0:
        st += str((num % 2))
        num /= 2
    return st

def largest_index(st):
    l = len(st)
    ind = None
    for j in range(l - 1, -1, -1):
        if st[j] == '0':
            ind = j
            break
    return ind


num = int(raw_input())
ops = 0
numbers = []

st = create_bits(num)




while True:
    ind = largest_index(st)
    
    xor_number = 2 ** (ind + 1) - 1 if ind is not None else None
    
    if xor_number:
        ops += 1
        num ^= xor_number
        
        numbers.append(ind + 1)

        if largest_index(create_bits(num)) is not None:
            num += 1
            
            ops += 1
            st = create_bits(num)
            
        else:
            break
    else:
        break

print(ops)
print(' '.join(str(n) for n in numbers))




